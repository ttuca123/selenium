package enumerators;

public enum EnumSituacao
{

	INATIVO(0, "INATIVO"),
	ATIVO(1, "ATIVO"),
	IGNORADO(2, "IGNORADO");

	private Integer numero;

	private String valor;

	private EnumSituacao(Integer numero, String valor)
	{
		this.numero = numero;
		this.valor = valor;

	}		

	public Integer getNumero()
	{
		return numero;
	}

	public void setNumero(Integer numero)
	{
		this.numero = numero;
	}

	public String getValor()
	{
		return valor;
	}

	public void setValor(String valor)
	{
		this.valor = valor;
	}
}
