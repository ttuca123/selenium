package enumerators;

public enum EnumDefFase
{
	TESTES(0, "DURANTES OS TESTES"),

	PUBLICACAO(1, "APÃ“S PUBLICACAO");

	private Integer codigo;

	private String descricao;

	private EnumDefFase(Integer codigo, String descricao)
	{
		this.codigo = codigo;
		this.descricao = descricao;
	}

	public Integer getCodigo()
	{
		return codigo;
	}

	public String getDescricao()
	{
		return descricao;
	}

	public void setCodigo(Integer codigo)
	{
		this.codigo = codigo;
	}

	public void setDescricao(String descricao)
	{
		this.descricao = descricao;
	}
}
